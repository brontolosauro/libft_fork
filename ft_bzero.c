#include "libft.h"
#include <stdlib.h>

void	ft_bzero(void *s, size_t len)
{
	size_t	i;
	char	*dummy;

	i = 0;
	dummy = s;
	while (i < len)
	{
		dummy[i] = '\0';
		i++;
	}
}
