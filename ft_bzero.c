#include <stdlib.h>
#include "libft.h"

void	ft_bzero(void *s, size_t len)
{
	size_t	i;

	if (s != NULL)
	{
		i = 0;
		while (i < len)
		{
			*((char *)s + i) = '\0';
			i++;
		}
	}
}
